#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(f)(void *), void (del)(void *))
{
	t_list	*new;
	t_list	*res;

	if (!lst)
		return (0);
	if (f == NULL)
		return (0);
	res = NULL;
	while (lst)
	{
		new = ft_lstnew(f(lst->content));
		if (!new)
		{
			ft_lstclear(&res, del);
			return (0);
		}
		ft_lstadd_back(&res, new);
		lst = lst->next;
	}
	return (res);
}
